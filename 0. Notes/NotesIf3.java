public class NotesIf3 {
    public static void main(String[] args) {
        int value1 = 20;
        int value2 = 30;

        if (value1 > value2) {
            System.out.println(value1 + " is greater than " + value2);
        }
        if (value1 < value2) {
            System.out.println(value1 + " is less than " + value2);
        }
        if (value1 == value2) {
            System.out.println(value1 + " is equal to " + value2);
        }
    }
}
